import math
import random

def squaredDistance(x, y):
    return (x[0] - y[0]) * (x[0] - y[0]) + \
           (x[1] - y[1]) * (x[1] - y[1]) + \
           (x[2] - y[2]) * (x[2] - y[2])

def normalized(x):
    im = 1. / math.sqrt(x[0] * x[0] + x[1] * x[1] + x[2] * x[2])
    return (x[0]*im, x[1]*im, x[2]*im)

def randomUnitVector():
    x = (random.random(), random.random(), random.random())
    return normalized(x)

